package jdbc;

public class Employee {
int Empcode;
String Empname;
double Salary;
public int getEmpcode() {
	return Empcode;
}
public void setEmpcode(int empcode) {
	Empcode = empcode;
}
public String getEmpname() {
	return Empname;
}
public void setEmpname(String empname) {
	Empname = empname;
}
public double getSalary() {
	return Salary;
}
public void setSalary(double salary) {
	Salary = salary;
}
}
